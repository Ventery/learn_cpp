#include<iostream>
using namespace std;
int main()
{
	char c1;
	short c2;
	int c3;
	long c4;
	float c5;
	double c6;
	long double c7;
	long long c8;
	cout<<"char "<<sizeof(c1)<<endl;
	cout<<"short "<<sizeof(c2)<<endl;
	cout<<"int "<<sizeof(c3)<<endl;
	cout<<"long "<<sizeof(c4)<<endl;
	cout<<"float "<<sizeof(c5)<<endl;
	cout<<"double "<<sizeof(c6)<<endl;
	cout<<"long double "<<sizeof(c7)<<endl;
	cout<<"long long "<<sizeof(c8)<<endl;
	return 0;
}
